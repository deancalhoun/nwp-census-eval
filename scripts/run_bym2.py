"""
scripts/run_bym2.py — BYM2 hierarchical spatial model for forecast bias.

Fits a Besag-York-Mollié 2 (BYM2) model in PyMC5 to test whether IFS 2m
temperature forecast bias correlates with demographic vulnerability indicators
after accounting for spatial autocorrelation and seasonal variation.

Model structure:
    bias_ij ~ Normal(mu_ij, sigma)
    mu_ij = alpha
           + gamma * season_j          # seasonal fixed effect (DOY harmonic)
           + beta @ demographics_i     # demographic fixed effects
           + phi_i                     # spatially structured random effect (ICAR)
           + theta_i                   # unstructured (IID) random effect
    (phi, theta) ~ BYM2(W, rho, sigma_s)

Inputs (generated by notebooks/03_prep_model_data.ipynb):
    notebooks/data/model_input.parquet  — (geo_id, lead_time, mean_bias, demographics...)
    notebooks/data/adjacency_W.npz      — sparse adjacency matrix (libpysal queen contiguity)
    notebooks/data/node_order.csv       — geo_id → node index mapping

Output:
    notebooks/data/bym2_trace_lead{N}h.nc  — ArviZ InferenceData (NetCDF4)
"""

import os
import numpy as np
import pandas as pd
import scipy.sparse as sp
import arviz as az

try:
    import pymc as pm
    import pytensor.tensor as pt
except ImportError as e:
    raise ImportError(
        "PyMC5 is required. Install with: conda install pymc"
    ) from e

DATA_DIR = os.path.join(os.path.dirname(__file__), "..", "notebooks", "data")
TRACE_PATH = os.path.join(DATA_DIR, "bym2_trace.nc")

# Lead time to model (hours); run separately for each lead time of interest
TARGET_LEAD = int(os.environ.get("BYM2_LEAD_TIME", "24"))

# MCMC settings
DRAWS = 1000
TUNE = 1000
CHAINS = 4
TARGET_ACCEPT = 0.9


def load_inputs(lead_time: int):
    df = pd.read_parquet(os.path.join(DATA_DIR, "model_input.parquet"))
    df = df[df["lead_time"] == lead_time].copy()

    node_order = pd.read_csv(os.path.join(DATA_DIR, "node_order.csv"))
    df = df.merge(node_order, on="geo_id", how="inner").sort_values("node_idx")

    W_sparse = sp.load_npz(os.path.join(DATA_DIR, "adjacency_W.npz"))
    # Edge list for ICAR prior
    rows, cols = W_sparse.nonzero()
    mask = rows < cols  # upper triangle only
    edge_list = np.stack([rows[mask], cols[mask]], axis=1)

    return df, edge_list


def build_model(df: pd.DataFrame, edge_list: np.ndarray):
    n = len(df)
    n_edges = len(edge_list)

    # Demographic covariates (standardise)
    demo_cols = [c for c in df.columns if c.startswith("demo_")]
    X = df[demo_cols].values.astype(float)
    X = (X - X.mean(axis=0)) / (X.std(axis=0) + 1e-8)

    # Seasonal harmonic (sin/cos of DOY)
    doy = df["day_of_year"].values
    season = np.stack([
        np.sin(2 * np.pi * doy / 365),
        np.cos(2 * np.pi * doy / 365),
    ], axis=1)

    y = df["mean_bias"].values.astype(float)

    with pm.Model() as model:
        # --- Priors ---
        alpha = pm.Normal("alpha", 0, 2)
        sigma = pm.HalfNormal("sigma", 1)

        # Seasonal
        gamma = pm.Normal("gamma", 0, 1, shape=2)
        season_effect = pt.dot(season, gamma)

        # Demographic fixed effects
        beta = pm.Normal("beta", 0, 1, shape=X.shape[1])
        demo_effect = pt.dot(X, beta)

        # BYM2 spatial random effect
        sigma_s = pm.HalfNormal("sigma_s", 1)
        rho = pm.Beta("rho", 0.5, 0.5)

        # ICAR component (phi) via sum-to-zero constraint
        phi_raw = pm.Normal("phi_raw", 0, 1, shape=n)
        phi = phi_raw - pt.mean(phi_raw)

        # IID component (theta)
        theta = pm.Normal("theta", 0, 1, shape=n)

        # BYM2 combination
        spatial = sigma_s * (pt.sqrt(rho) * phi + pt.sqrt(1 - rho) * theta)

        # ICAR potential (soft constraint via edge differences)
        edge_diffs = phi[edge_list[:, 0]] - phi[edge_list[:, 1]]
        pm.Potential("icar_prior", -0.5 * pt.sum(edge_diffs ** 2))

        # Likelihood
        mu = alpha + season_effect + demo_effect + spatial
        pm.Normal("bias_obs", mu=mu, sigma=sigma, observed=y)

    return model


def main():
    os.makedirs(DATA_DIR, exist_ok=True)
    print(f"Loading model inputs for lead_time={TARGET_LEAD}h ...")
    df, edge_list = load_inputs(TARGET_LEAD)
    print(f"  {len(df)} counties | {len(edge_list)} edges")

    model = build_model(df, edge_list)

    print(f"Sampling: {CHAINS} chains × {DRAWS} draws + {TUNE} tune ...")
    with model:
        trace = pm.sample(
            draws=DRAWS,
            tune=TUNE,
            chains=CHAINS,
            target_accept=TARGET_ACCEPT,
            progressbar=True,
        )

    trace_path = TRACE_PATH.replace(".nc", f"_lead{TARGET_LEAD}h.nc")
    az.to_netcdf(trace, trace_path)
    print(f"Saved trace → {trace_path}")

    # Quick summary
    summary = az.summary(trace, var_names=["alpha", "sigma", "sigma_s", "rho", "gamma", "beta"])
    print(summary.to_string())


if __name__ == "__main__":
    main()
